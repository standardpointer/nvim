local function normalize_header(header, width)
    local lines = vim.split(header, "\n", { trimempty = false })
    local max_len = math.max(unpack(vim.tbl_map(function(line)
        return #line
    end, lines)))

    -- Use the provided width or the max found
    local target_width = width or max_len

    for i, line in ipairs(lines) do
        local padding = math.floor((target_width - #line) / 2)
        lines[i] = string.rep(" ", padding) .. line
    end

    return table.concat(lines, "\n")
end

return setmetatable(headers, {
    __index = function(tb, key)
        if key == "random" then
            -- Choose a random sub-table inside headers (imgs | banners)
            local ks = vim.tbl_keys(tb)
            local sub = tb[ks[math.random(#ks)]]

            -- Obtain keys and return a random banner
            local keys = vim.tbl_keys(sub)
            return sub[keys[math.random(1, #keys)]]
        else
            -- Return the desired banner
            return tb[key]
        end
    end,
})
